function docTipo(doc) {
  if (!doc || doc.trim() === '' || doc.trim() === '.' || doc.trim() === ',')
    return [null, null]
  const d = doc.trim()
  if (d.includes('/')) return ['cgc', d]
  if (d.includes('-') && d.replace(/[\.\-]/g, '').length === 11)
    return ['cpf', d]
  return ['cgc', d]
}

