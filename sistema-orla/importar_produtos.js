const stmt = db.prepare(`
  INSERT OR IGNORE INTO produtos (
    codigo, descricao, codigo_linha, ean, unidade,
    preco_custo_atual, preco_venda_vista, preco_venda_prazo,
    situacao_produto, revenda_consumo,
    ncm,
    ibpt_percentual_nacional, ibpt_percentual_importado,
    ibpt_percentual_estadual, ibpt_percentual_municipal,
    cfop_cfe, aliquota_icms,
    aliquota_pis, aliquota_cofins,
    controla_estoque, estoque_atual, estoque_minimo,
    data_atualizacao, hora_atualizacao
  ) VALUES (
    ?, ?, ?, ?, ?,
    ?, ?, ?,
    'A', 'R',
    ?,
    ?, ?, ?, 0,
    '5405', 500,
    99, 99,
    'S', 0, 0,
    date('now'), time('now')
  )
`)

const inserirTodos = db.transaction(() => {
  let count = 0
  for (const [
    codigo,
    descricao,
    linha,
    ean,
    unidade,
    custo,
    vista,
    prazo,
    ncm,
    ibpt_nac,
    ibpt_imp,
    ibpt_est,
  ] of produtos) {
    stmt.run(
      codigo,
      descricao,
      linha,
      ean,
      unidade,
      custo,
      vista,
      prazo,
      ncm,
      ibpt_nac,
      ibpt_imp,
      ibpt_est,
    )
    count++
  }
  return count
})

const total = inserirTodos()
console.log(`✅ Importado! ${total} produtos inseridos.`)
db.close()
